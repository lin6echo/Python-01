# range(n) - 1 Parameter Form
s = 'Python'
len(s)
for i in range(len(s)):
    print(i, s[i])
    
# reversed() Variation
s = 'Python'
len(s)

for i in reversed(range(len(s))):
    print(i, s[i])
    
# range(start, stop) - 2 Parameter Form
x = list(range(2,10))
print(x)

# range(start, stop, step) - 3 Parameter Form
x = list(range(2,20,2)) 
print(x)

x = range(5)
for n in x:
  print(n)

